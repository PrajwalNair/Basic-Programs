class Operators3{
public static void main(String [] args){
		/*Logical Operators*/
		int x=12;
		int y=16;
		
		boolean result = x>y && y>x;
		boolean output = x>y || y>x;
		boolean c = x!=1;
		
		System.out.println(result);
		System.out.println(output);
		System.out.println(c);
		System.out.println(x>y && y>x);
		System.out.println(x>y || y>x);
		System.out.println(x!=y);
		


}


}