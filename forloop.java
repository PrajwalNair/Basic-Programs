class forloop
{
	public static void main (String [] args)
	{
		
		// to print even numbers from 1-10
		
		for (int x=2; x<=10 ; x+=2)
		{
			System.out.println(x);
		}
		
		
		for (int x=1; x<=10 ; x+=2)
		{
			System.out.println(x);
		}
		
		for (int x=15; x<=150 ; x+=15)
		{
			System.out.println(x);
		}
	}

}