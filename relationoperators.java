class relationoperators
{
public static void main(String[] args)
	{
		int a = 5, b = 6;
		
		 // value of a & b //
		 System.out.println("a = "+a+" b = "+b);
		 
		 // == operator //
		 System.out.println(a==b);
		 
		 // > operator //
		 System.out.println(a>b);
		 
		  // < operator //
		 System.out.println(a<b);
		 
		  // >= operator //
		 System.out.println(a>=b);
		 
		  // <= operator //
		 System.out.println(a<=b);
		 
		  // != operator //
		 System.out.println(a!=b);
		 
		 
		
	}



} 