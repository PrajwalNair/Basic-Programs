class Umbrella
{
	static void protectFromRain ()
	{
		System.out.println("Umbrella protects from rain in the rainy season");
		
		open ();
		
	}
	static void protectFromSun ()
	{
		System.out.println("Umbrella protects from sun in the summer season");
		
		open ();
		
	}
	static void shut ()
	{
		System.out.println("If there is no rain shut the umbrella");
	}
	static void open ()
	{
		System.out.println("If it is a sunny day then open the umbrella");
	}


}