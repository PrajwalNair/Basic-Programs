class Operators4{
public static void main(String []args){
		//Unary Operators//
		int x = 10;
		int y = x++;
		System.out.println(x+" "+y);
		int a = 10;
		System.out.println("a "+a);
		
		a++;
		System.out.println("a "+a);
		
		a++;
		System.out.println("a "+a);
		
		a++;
		System.out.println("a "+a);
		
		a--;
		System.out.println("a "+a);
		
		--a;
		System.out.println("a "+a);
		
		int p = 12;
		int q = ++p;
		System.out.println(p+"     "+q);
		
		int r = 15;
		int s = --r;
		System.out.println(r+"     "+s);
		
		int e = 12;
		int f = e--;
		System.out.println(e+"     "+f);
		
		
		
	
		
		




}
}