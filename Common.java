class Common
{
	public static void main(String[] args)//int
	{
		
		int a=Integer.compareUnsigned(-12,30);
		System.out.println(a);
		int b=Integer.min(26,85);
		System.out.println(b);
		int c=Integer.compare(75,8);
		System.out.println(c);
		int d=Integer.decode("958");
		System.out.println(d);
		int e=Integer.divideUnsigned(1,4);
		System.out.println(e);
		int f=Integer.bitCount(56);
		System.out.println(f);
		int g=Integer.max(52,38);
		System.out.println(g);
		int h=Integer.parseInt("74");
		System.out.println(h);
		int i=Integer.reverseBytes(9);
		System.out.println(i);
		int j=Integer.sum(3,15);
		System.out.println(j);
	}
	
}