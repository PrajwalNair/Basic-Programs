class assignmentoperators
{
	public static void main(String [] args)
	{
		// creates variables//
		
		int x = 20;
		int  age ;
		
		// assign value using//
		
		age = x;
		System.out.println("age of the person : " +age );
		
		// assign value using += // 
		
		age += x;
		System.out.println("age of the person : " +age );
		
		// assign value using *= // 
		
		age %= x;
		System.out.println("age of the person : " +age );
		
	}




}