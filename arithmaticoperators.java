class arithmaticoperators
{
	public static void main(String [] args)
	{
	int a = 10;
	float b = 2f;
	
	//addition operators //
	System.out.println("a+b = "+(a+b));
	
	// subtraction operators//
	System.out.println("a-b = "+(a-b));
	
	// multiplication operators//
	System.out.println("a*b = "+(a*b));
	
	// division operators//
	System.out.println("a/b = "+(a/b));
	
	// modulo operators//
	System.out.println("a%b = "+(a%b));
	
	}


}